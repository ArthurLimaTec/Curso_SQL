/*FILTROS DE CONTADORES*/

SELECT COUNT(*) FROM FUNCIONARIOS;

/*FILTRANDO NÚMERO DE FUNCIONÁRIOS E OS DO SEXO MASCULINO.

FORMA PIOR E LONGA:*/
SELECT COUNT(*) AS "QUANTIDADE TOTAL", (SELECT COUNT(*)
FROM FUNCIONARIOS
WHERE SEXO = 'Masculino'
GROUP BY SEXO) AS "Masculino"
FROM FUNCIONARIOS;

/*FORMA MAIS FACIL E MELHOR: ISSO SERVE PRA FILTRAR QUALQUER COISA!!!*/

SELECT COUNT(*) AS "QUANTIDADE TOTAL",
COUNT(*) FILTER(WHERE SEXO = 'Masculino') AS "MASCULINO",
COUNT(*) FILTER(WHERE DEPARTAMENTO = 'Books') AS "BOOKS",
COUNT(*) FILTER(WHERE SALARIO > 90000) AS "SALARIO > 90K"
FROM FUNCIONARIOS;
